%get weightMatrix (P x N size)
function WeightMatrix = GetWeights(patterns)
    Npatterns = size(patterns,1);   
    Nbits = size(patterns,2);
    WeightMatrix = zeros(Nbits,Nbits);
    
    for iPattern = 1:Npatterns
        patternI = patterns(iPattern,:);
        WeightMatrix = WeightMatrix+mtimes(patternI',patternI);
    end
    WeightMatrix = WeightMatrix/Nbits;
    
    %modified hebbs rule
    for iBits = 1:Nbits
        WeightMatrix(iBits,iBits) = 0;
    end
end

%asynchronous update of state N times
function newState = UpdateState(currentState,weightMatrix)

    newState = currentState;
    %N asynchronous updates in a row
    for iBit = 1:length(currentState)
        newState(iBit) = Sgn(weightMatrix(iBit,:)*currentState');
        currentState(iBit) = newState(iBit);
    end

end


%sign(x) with if==0 --> =1
function sgn = Sgn(x)
    sgn = sign(x);
    if sgn == 0 
        sgn = 1;
    end
end

